# Задача №2949. Обмен значений

# Напишите программу, которая считывает значения двух целочисленных переменных a и b, затем меняет их значения
# местами (то есть в переменной a должно быть записано то, что раньше хранилось в b, а в переменной b записано то,
# что раньше хранилось в a). Затем выведите значения переменных.
#
# Входные данные
# Вводятся целые числа a и b.
#
# Выходные данные
# Выведите ответ на задачу.

# Примеры
# входные данные
# 8
# 5
# выходные данные
# 5 8
