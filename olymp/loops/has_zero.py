# Задача №347. Ноль или не ноль
#
# Проверьте, есть ли среди данных N чисел нули.
#
# Входные данные
# Вводится число N, а затем N чисел.
#
# Выходные данные
# Выведите YES, если среди введенных чисел есть хотя бы один нуль, или NO в противном случае.
#
# Примеры
# Входные данные
# 3
# 28
# 1
# 0
# Выходные данные
# YES
# Входные данные
# 1
# 28
# Выходные данные
# NO
