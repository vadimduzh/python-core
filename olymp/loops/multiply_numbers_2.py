# Задача. Перемножение чисел

# По данному натуральному n перемножьте число (k + 2) n раз, т.е. (k+2) * (k+2) * ... * (k+2) и так n раз
#
# Входные данные
# Вводится целые положительные числа n и к
#
# Выходные данные
# Необходимо вывести вычисленную сумму.
#
# Примеры
# Входные данные
# 4
# 1
# Выходные данные
# 16
#
# Входные данные#
# 3
# 2
# Выходные данные
# 125
